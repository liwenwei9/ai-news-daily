    def get_arxiv_papers(self) -> List[Dict[str, Any]]:
        print("📚 获取arXiv论文...")
        papers = []
        try:
            client = arxiv.Client()
            search = arxiv.Search(
                query="cat:cs.AI OR cat:cs.LG",
                max_results=5,
                sort_by=arxiv.SortCriterion.SubmittedDate,
                sort_order=arxiv.SortOrder.Descending
            )
            for res in client.results(search):
                papers.append({
                    "id": res.entry_id.split('/')[-1],
                    "title": res.title,
                    "authors": [a.name for a in res.authors[:3]],
                    "summary": res.summary,
                    "published": res.published.strftime('%Y-%m-%d'),
                    "pdf_url": res.pdf_url,
                    "arxiv_url": f"https://arxiv.org/abs/{res.entry_id.split('/')[-1]}",
                    "category": res.primary_category
                })
            print(f"✅ 获取到 {len(papers)} 篇论文")
        except Exception as e:
            print(f"⚠️ 获取论文失败: {e}")
        return papers

    def get_tech_news(self) -> List[Dict[str, Any]]:
        print("📰 获取科技新闻...")
        news = []
        try:
            feed = feedparser.parse('https://www.technologyreview.com/feed/')
            for entry in feed.entries[:5]:
                news.append({
                    "title": entry.title,
                    "source": "MIT Technology Review",
                    "link": entry.link,
                    "summary": entry.get('summary', ''),
                    "published": entry.get('published', datetime.datetime.now().strftime('%Y-%m-%d')),
                })
            print(f"✅ 获取到 {len(news)} 条新闻")
        except Exception as e:
            print(f"⚠️ 获取新闻失败: {e}")
        return news

