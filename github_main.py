    def generate_one_sentence_summary(self, text: str, max_length: int = 60) -> str:
        """
        生成一句话概要
        简化版本：提取第一句话或前N个字符
        未来可以接入AI API进行智能总结
        """
        # 移除多余空白
        text = ' '.join(text.split())

        # 尝试提取第一句话
        sentences = re.split(r'[.。!?！？]', text)
        if sentences and len(sentences[0]) > 10:
            first_sentence = sentences[0].strip()
            if len(first_sentence) <= max_length:
                return first_sentence
            else:
                # 如果第一句话太长，截取关键部分
                return first_sentence[:max_length] + '...'

        # 如果没有合适的句子，直接截取
        if len(text) <= max_length:
            return text
        return text[:max_length] + '...'

    def get_arxiv_papers(self) -> List[Dict[str, Any]]:
        """从arXiv获取AI论文"""
        print("📚 获取arXiv论文...")

        papers = []
        try:
            client = arxiv.Client()
            search = arxiv.Search(
                query="cat:cs.AI OR cat:cs.LG",
                max_results=10,  # 获取更多以便筛选
                sort_by=arxiv.SortCriterion.SubmittedDate,
                sort_order=arxiv.SortOrder.Descending
            )

            for result in client.results(search):
                # 生成一句话概要
                one_sentence = self.generate_one_sentence_summary(result.title)

                paper = {
                    'id': result.entry_id.split('/')[-1],
                    'title': result.title,
                    'one_sentence': one_sentence,  # 新增：一句话概要
                    'authors': [author.name for author in result.authors[:3]],
                    'summary': result.summary,
                    'published': result.published.strftime('%Y-%m-%d'),
                    'pdf_url': result.pdf_url,
                    'arxiv_url': f"https://arxiv.org/abs/{result.entry_id.split('/')[-1]}",
                    'category': result.primary_category
                }
                papers.append(paper)

                # 只保留前5篇
                if len(papers) >= 5:
                    break

            print(f"✅ 获取到 {len(papers)} 篇论文（从10篇中筛选）")

        except Exception as e:
            print(f"⚠️ 获取论文失败: {e}")
