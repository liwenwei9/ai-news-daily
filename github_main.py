    def _generate_news_html(self, news: List[Dict]) -> str:
        """生成新闻HTML（改进版）"""
        if not news:
            return '<p class="empty-message" data-zh="暂无新闻数据" data-en="No news available">暂无新闻数据</p>'

        html_parts = []
        for idx, item in enumerate(news, 1):
            html = f'''
            <div class="item">
                <div class="item-header">
                    <div class="item-number">{idx:02d}</div>
                    <div class="item-content">
                        <h3 class="item-title">{item['title']}</h3>
                        <div class="item-one-sentence">{item['one_sentence']}</div>
                        <div class="item-meta">
                            <div class="meta-item">
                                <span>🏢</span>
                                <span data-zh="来源" data-en="Source">来源</span>: {item['source']}
                            </div>
                            <div class="meta-item">
                                <span>📅</span>
                                <span>{item['published']}</span>
                            </div>
                        </div>
                    </div>
                </div>
                <div class="item-summary">{item['summary']}</div>
                <div class="item-actions">
                    <a href="{item['link']}" class="btn" target="_blank">
                        <span>🔗</span>
                        <span data-zh="阅读原文" data-en="Read Original">阅读原文</span>
                    </a>
                </div>
            </div>
            '''
            html_parts.append(html)

        return '\n'.join(html_parts)

